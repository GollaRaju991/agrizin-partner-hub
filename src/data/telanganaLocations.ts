// Helper to get Telangana district names for the main location system
export const getTelanganaDistrictNames = (): string[] =>
  telanganaDistricts.map((d) => d.name).sort();

// Helper to get mandal names for a Telangana district (by district name)
export const getTelanganaMandals = (districtName: string): string[] => {
  const district = telanganaDistricts.find((d) => d.name === districtName);
  if (!district) return [];
  const mandals = telanganaMandals[district.code];
  return mandals ? mandals.map((m) => m.name).sort() : [];
};

// Helper to get Telugu name for a district
export const getTelanganaDistrictTelugu = (districtName: string): string => {
  const district = telanganaDistricts.find((d) => d.name === districtName);
  return district?.nameTe || districtName;
};

// Helper to get Telugu name for a mandal
export const getTelanganMandalTelugu = (districtName: string, mandalName: string): string => {
  const district = telanganaDistricts.find((d) => d.name === districtName);
  if (!district) return mandalName;
  const mandals = telanganaMandals[district.code];
  const mandal = mandals?.find((m) => m.name === mandalName);
  return mandal?.nameTe || mandalName;
};
